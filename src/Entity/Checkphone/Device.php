<?php

namespace App\Entity\Checkphone;

use App\Repository\Checkphone\DeviceRepository;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=DeviceRepository::class)
 * @ORM\Table(name="my_entity",
 *     uniqueConstraints={@ORM\UniqueConstraint(name="unique_map_idx", columns={"uid"})})
 */
class Device
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=25)
     */
    private $uid;

    /**
     * @ORM\Column(type="smallint", length=25)
     */
    private $try;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getUid(): ?string
    {
        return $this->uid;
    }

    public function setUid(string $uid): self
    {
        $this->uid = $uid;

        return $this;
    }

    public function getTry(): ?int
    {
        return $this->try;
    }

    public function setTry(int $try): self
    {
        $this->try = $try;

        return $this;
    }
}
